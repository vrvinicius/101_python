# soma
print("1 + 1 = ", 1 + 1)

# subtração
print("10 - 5 = ", 1 - 1)

# multiplicação
print("2 x 3 = ", 2 * 3)

# divisão
print("10 / 2 = ", 10 / 2)

# divisão inteira
print("10 // 3 = ", 10 // 3)

# resto da divisão
print("10 % 4 = ", 10 % 4)

# potenciação
print("2 ** 3 = ", 2 ** 3)

# raiz quadrada
print("Raiz quadrada de 16 = ", 16 ** (1/2)) # numero ** 0.5